int		nmatch(char *st1, char *st2)
{
	if (*st2 == '*')
	{
		if (*st1)
			return (nmatch(st1 + 1, st2) + nmatch(st1, st2 + 1));
		return (nmatch(st1, st2 + 1));
	}
	if (*st1 == *st2)
	{
		if (*st1)
			return (nmatch(st1 + 1, st2 + 1));
		return (1);
	}
	return (0);
}
